// alert_markup
function alert_markup(alert_type, msg) {
    return '<div class="alert alert-' + alert_type + '" role="alert">' + msg + '<button type="button" class="close" data-dismiss="alert" aria-label="Close"><span>&times;</span></button></div>';
}

function addIDToURL(baseurl, path) {
    var url = document.location.href,
        params = url.split('#')[0].split('?')[1].split('&'),
        data = {}, tmp;
    for (var i = 0, l = params.length; i < l; i++) {
         tmp = params[i].split('=');
         data[tmp[0]] = tmp[1];
    }
    var id = data.id;
    var newURL = baseurl;

    if(id){
        newURL = newURL+'?id='+id+path;
    }

    window.location=newURL;
}

// Validate Code
var validate_code = function (code) {
    var app_url = "https://script.google.com/macros/s/AKfycbw-cm-6UMmdf5Gq_OL_n9tgWKNZaH6fmzPUdWbsP3bqjdsbt87AL50Y87I-vLgGfXE/exec";
    app_url = app_url + "?code=" + code + "&verifyonly=" + true;
    var valid_code = false;
    $.ajax({
        url: app_url,
        method: "GET",
        async: false,
        dataType: "json",
        success: function (response) {
            if (response.valid === true) {
                valid_code = true;
            } else {
                valid_code = false;
            }
        },
        error: function () {
            alert("The form failed. Please let Jonathan know and try again later.")
            valid_code = false
        }
    });
    return valid_code;
};
